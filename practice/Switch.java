package practice;

public class Switch {

	public static void main(String[] args) {
		
		int option=4;
		
		switch (option) {
		
		case 1:
			System.out.println("print 1");
			break;
		case 2:
			System.out.println("print 2");
			break;
		case 3:
			System.out.println("print 3");
			break;
		default:
			System.out.println("print default");
			
		}

	}

}
